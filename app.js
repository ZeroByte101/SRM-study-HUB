function runLogicGateSimulation() {
  const gateNode = byId("logicGateType");
  const aNode = byId("logicInputA");
  const bNode = byId("logicInputB");
  const resultNode = byId("logicResult");
  if (!gateNode || !aNode || !bNode || !resultNode) return;

  const gate = String(gateNode.value || "AND").toUpperCase();
  const a = Number(aNode.value);
  const b = Number(bNode.value);
  let out = 0;
  let expression = "";

  switch (gate) {
    case "AND":
      out = a & b;
      expression = `${a} AND ${b}`;
      break;
    case "OR":
      out = a | b;
      expression = `${a} OR ${b}`;
      break;
    case "XOR":
      out = a ^ b;
      expression = `${a} XOR ${b}`;
      break;
    case "NAND":
      out = (a & b) ? 0 : 1;
      expression = `${a} NAND ${b}`;
      break;
    case "NOR":
      out = (a | b) ? 0 : 1;
      expression = `${a} NOR ${b}`;
      break;
    case "XNOR":
      out = (a ^ b) ? 0 : 1;
      expression = `${a} XNOR ${b}`;
      break;
    case "NOT":
      out = a ? 0 : 1;
      expression = `NOT ${a}`;
      break;
    default:
      out = a & b;
      expression = `${a} AND ${b}`;
      break;
  }

  resultNode.textContent = `Result: ${expression} = ${out}`;
}

function parseBinaryBigInt(rawValue, label) {
  const text = String(rawValue || "").trim();
  if (!text) return { ok: false, error: `Enter ${label} binary value.` };
  if (!/^[01]+$/.test(text)) return { ok: false, error: `${label} must contain only 0 and 1.` };
  try {
    return { ok: true, text, value: BigInt(`0b${text}`) };
  } catch (_err) {
    return { ok: false, error: `${label} is too large to parse.` };
  }
}

function formatBinaryBigInt(value) {
  if (typeof value !== "bigint") return "-";
  if (value < 0n) return `-${(-value).toString(2)}`;
  return value.toString(2);
}

function runBinaryOperations() {
  const opNode = byId("binaryOpType");
  const aNode = byId("binaryInputA");
  const bNode = byId("binaryInputB");
  const statusNode = byId("binaryOpsStatus");
  const binNode = byId("binaryOpsBinary");
  const decNode = byId("binaryOpsDecimal");
  if (!opNode || !aNode || !bNode || !statusNode || !binNode || !decNode) return;

  const rawA = String(aNode.value || "").trim();
  const rawB = String(bNode.value || "").trim();
  if (!rawA && !rawB) {
    statusNode.textContent = "";
    binNode.textContent = "-";
    decNode.textContent = "-";
    return;
  }

  const aParsed = parseBinaryBigInt(rawA, "A");
  if (!aParsed.ok) {
    statusNode.textContent = aParsed.error;
    binNode.textContent = "-";
    decNode.textContent = "-";
    return;
  }

  const bParsed = parseBinaryBigInt(rawB, "B");
  if (!bParsed.ok) {
    statusNode.textContent = bParsed.error;
    binNode.textContent = "-";
    decNode.textContent = "-";
    return;
  }

  const operation = String(opNode.value || "and");
  let result = 0n;
  let label = "";

  switch (operation) {
    case "and":
      result = aParsed.value & bParsed.value;
      label = "AND";
      break;
    case "or":
      result = aParsed.value | bParsed.value;
      label = "OR";
      break;
    case "xor":
      result = aParsed.value ^ bParsed.value;
      label = "XOR";
      break;
    case "add":
      result = aParsed.value + bParsed.value;
      label = "ADD";
      break;
    case "sub":
      result = aParsed.value - bParsed.value;
      label = "SUBTRACT";
      break;
    default:
      result = aParsed.value & bParsed.value;
      label = "AND";
      break;
  }

  binNode.textContent = formatBinaryBigInt(result);
  decNode.textContent = result.toString();
  statusNode.textContent = `${label} complete.`;
}

