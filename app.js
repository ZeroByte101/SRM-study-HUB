function calculateGpa() {
  const rows = Array.from(document.querySelectorAll("#gpaRows .grade-row"));
  const result = byId("gpaResult");
  if (!result) return;

  if (!rows.length) {
    result.textContent = "Add at least one subject row.";
    return;
  }

  let totalCredits = 0;
  let weightedPoints = 0;

  for (const row of rows) {
    const creditInput = row.querySelector(".gpa-credit");
    const gradeSelect = row.querySelector(".gpa-grade");
    const credit = Number(creditInput ? creditInput.value : "");
    const point = Number(gradeSelect ? gradeSelect.value : "");

    if (Number.isNaN(credit) || credit <= 0) {
      result.textContent = "Enter valid credits (> 0) for all subjects.";
      return;
    }

    totalCredits += credit;
    weightedPoints += credit * point;
  }

  const gpa = weightedPoints / totalCredits;
  result.textContent = `GPA: ${gpa.toFixed(2)} (Total Credits: ${totalCredits})`;
}

