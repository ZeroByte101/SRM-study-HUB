function renderList(id, items) {
  const node = byId(id);
  if (!node) return;
  node.innerHTML = "";
  const fragment = document.createDocumentFragment();
  items.forEach((item) => {
    const li = document.createElement("li");
    li.textContent = item;
    fragment.appendChild(li);
  });
  node.appendChild(fragment);
}

