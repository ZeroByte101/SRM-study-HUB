function parseInputByBase(rawValue, base) {
  let text = String(rawValue || "").trim().toLowerCase();
  if (!text) return { ok: false, error: "Enter a value to convert." };

  let sign = 1;
  if (text.startsWith("-")) {
    sign = -1;
    text = text.slice(1);
  } else if (text.startsWith("+")) {
    text = text.slice(1);
  }

  if (base === 2 && text.startsWith("0b")) text = text.slice(2);
  if (base === 16 && text.startsWith("0x")) text = text.slice(2);
  if (!text) return { ok: false, error: "Enter a valid number." };

  const validByBase = {
    2: /^[01]+$/,
    10: /^[0-9]+$/,
    16: /^[0-9a-f]+$/i
  };

  const validator = validByBase[base];
  if (!validator || !validator.test(text)) {
    return { ok: false, error: `Input is not valid for base ${base}.` };
  }

  const parsed = parseInt(text, base);
  if (Number.isNaN(parsed)) return { ok: false, error: "Unable to parse number." };
  return { ok: true, value: sign * parsed };
}

function convertNumberValue() {
  const input = byId("converterInput");
  const baseNode = byId("converterBase");
  const status = byId("converterStatus");
  const decOut = byId("converterDec");
  const binOut = byId("converterBin");
  const hexOut = byId("converterHex");
  if (!input || !baseNode || !status || !decOut || !binOut || !hexOut) return;

  const base = Number(baseNode.value);
  const parsed = parseInputByBase(input.value, base);

  if (!parsed.ok) {
    status.textContent = parsed.error;
    decOut.textContent = "-";
    binOut.textContent = "-";
    hexOut.textContent = "-";
    return;
  }

  const value = parsed.value;
  const abs = Math.abs(value);
  const signPrefix = value < 0 ? "-" : "";

  decOut.textContent = String(value);
  binOut.textContent = `${signPrefix}${abs.toString(2)}`;
  hexOut.textContent = `${signPrefix}${abs.toString(16).toUpperCase()}`;
  status.textContent = "Conversion complete.";
}

