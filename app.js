async function saveProfileAttendance() {
  const subjectInput = byId("profileSubject");
  const attendedInput = byId("profileAttended");
  const totalInput = byId("profileTotal");
  const status = byId("profileAttendanceStatus");
  if (!subjectInput || !attendedInput || !totalInput || !status) return;

  if (!getAuthToken()) {
    status.textContent = "Sign in first to save private attendance.";
    return;
  }

  const subject = subjectInput.value.trim();
  const attended = Number(attendedInput.value);
  const total = Number(totalInput.value);

  if (!subject) {
    status.textContent = "Enter subject name.";
    return;
  }
  if (Number.isNaN(attended) || attended < 0) {
    status.textContent = "Enter valid attended classes.";
    return;
  }
  if (Number.isNaN(total) || total <= 0) {
    status.textContent = "Enter valid total classes (> 0).";
    return;
  }
  if (attended > total) {
    status.textContent = "Attended classes cannot exceed total classes.";
    return;
  }

  status.textContent = "Saving attendance...";
  try {
    const { response, data } = await fetchJsonWithAuth("/profile/attendance", {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify({ subject, attended, total })
    });

    if (!response.ok) {
      status.textContent = data && data.error ? data.error : "Failed to save attendance.";
      return;
    }

    status.textContent = "Attendance saved to your private profile.";
    attendedInput.value = "";
    totalInput.value = "";
    await loadProfile();
  } catch (_err) {
    status.textContent = "Network error while saving profile attendance.";
  }
}

