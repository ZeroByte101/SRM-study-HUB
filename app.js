function getHelpBotReply(question) {
  const text = question.trim().toLowerCase();
  if (!text) {
    return {
      answer:
        "Ask me about resources, GradePilot+, attendance, chat, AI tutor, theme toggle, or a full website tour."
    };
  }

  let best = null;
  let bestScore = 0;

  HELP_BOT_TOPICS.forEach((topic) => {
    const score = topic.keys.reduce(
      (total, key) => (text.includes(key) ? total + 1 : total),
      0
    );
    if (score > bestScore) {
      bestScore = score;
      best = topic;
    }
  });

  if (bestScore > 0) return best;

  if (text.includes("hi") || text.includes("hello")) {
    return {
      answer:
        "Hi. Ask me what you want to do in this website, and I will point you to the correct section."
    };
  }

  return {
    answer:
      "I can help with resources, attendance, GradePilot+, Study Plus chat, AI tutor, theme switch, and website navigation."
  };
}

