function getApiBase() {
  const PRODUCTION_API_BASE = "https://srm-study-hub-backend-production.up.railway.app";
  if (typeof window === "undefined") return PRODUCTION_API_BASE;

  const host = String(window.location.hostname || "").toLowerCase();
  const isLocalHost = host === "localhost" || host === "127.0.0.1";
  if (isLocalHost) return "";

  return PRODUCTION_API_BASE;
}

const API_BASE = getApiBase();

function apiUrl(pathname) {
  const raw = String(pathname || "").trim();
  if (!raw) return API_BASE || "";
  if (/^https?:\/\//i.test(raw)) return raw;
  const safePath = raw.startsWith("/") ? raw : `/${raw}`;
  return `${API_BASE}${safePath}`;
}

function getProfilePhotoStorageKey(user) {
  const userId = user && user.id ? String(user.id) : "";
  if (!userId) return "";
  return `${PROFILE_PHOTO_STORAGE_PREFIX}:${userId}`;
}

function getStoredProfilePhoto(user) {
  const key = getProfilePhotoStorageKey(user);
  if (!key) return "";
  try {
    return localStorage.getItem(key) || "";
  } catch (_err) {
    return "";
  }
}

function setStoredProfilePhoto(user, dataUrl) {
  const key = getProfilePhotoStorageKey(user);
  if (!key) return;
  try {
    if (dataUrl) localStorage.setItem(key, dataUrl);
    else localStorage.removeItem(key);
  } catch (_err) {
    // Ignore storage failures for optional avatar photo.
  }
}

function getUserInitials(user) {
  const rawName = String(user?.name || "").trim();
  if (rawName) {
    const parts = rawName.split(/\s+/).filter(Boolean).slice(0, 2);
    const initials = parts.map((part) => part[0] || "").join("").toUpperCase();
    if (initials) return initials;
  }

  const email = String(user?.email || "").trim();
  if (email) return email[0].toUpperCase();
  return "S";
}

function applyProfileAvatar(photoUrl, initials) {
  const fabImage = byId("profileFabImage");
  const fabInitials = byId("profileFabInitials");
  const modalImage = byId("profileModalImage");
  const modalInitials = byId("profileModalInitials");
  const fallback = (initials || "S").slice(0, 2).toUpperCase();

  if (photoUrl) {
    if (fabImage) {
      fabImage.src = photoUrl;
      fabImage.hidden = false;
    }
    if (modalImage) {
      modalImage.src = photoUrl;
      modalImage.hidden = false;
    }
    if (fabInitials) {
      fabInitials.hidden = true;
      fabInitials.textContent = fallback;
    }
    if (modalInitials) {
      modalInitials.hidden = true;
      modalInitials.textContent = fallback;
    }
    return;
  }

  if (fabImage) {
    fabImage.src = "";
    fabImage.hidden = true;
  }
  if (modalImage) {
    modalImage.src = "";
    modalImage.hidden = true;
  }
  if (fabInitials) {
    fabInitials.hidden = false;
    fabInitials.textContent = fallback;
  }
  if (modalInitials) {
    modalInitials.hidden = false;
    modalInitials.textContent = fallback;
  }
}

function renderProfileIdentity() {
  const card = byId("profileIdentityCard");
  const nameNode = byId("profileIdentityName");
  const metaNode = byId("profileIdentityMeta");
  const photoInput = byId("profilePhotoInput");

  const initials = getUserInitials(currentUser);
  const photoUrl = currentUser ? getStoredProfilePhoto(currentUser) : "";
  applyProfileAvatar(photoUrl, initials);

  if (!currentUser) {
    if (card) card.hidden = true;
    if (nameNode) nameNode.textContent = "Student";
    if (metaNode) metaNode.textContent = "";
    if (photoInput) photoInput.value = "";
    return;
  }

  if (card) card.hidden = false;
  if (nameNode) nameNode.textContent = currentUser.name || currentUser.email || "Student";
  if (metaNode) {
    const parts = [];
    if (currentUser.studentId) parts.push(`ID: ${currentUser.studentId}`);
    if (currentUser.email) parts.push(currentUser.email);
    metaNode.textContent = parts.join(" | ");
  }
}

function setAuthPopupMode(mode) {
  authPopupMode = mode === "signup" ? "signup" : "signin";

  const showSignInBtn = byId("showSignInBtn");
  const showSignUpBtn = byId("showSignUpBtn");
  if (showSignInBtn) {
    const active = authPopupMode === "signin";
    showSignInBtn.classList.toggle("active", active);
    showSignInBtn.setAttribute("aria-selected", String(active));
  }
  if (showSignUpBtn) {
    const active = authPopupMode === "signup";
    showSignUpBtn.classList.toggle("active", active);
    showSignUpBtn.setAttribute("aria-selected", String(active));
  }

  if (currentUser) return;
  const signInForm = byId("signInForm");
  const signUpForm = byId("signUpForm");
  if (signInForm) signInForm.hidden = authPopupMode !== "signin";
  if (signUpForm) signUpForm.hidden = authPopupMode !== "signup";
}

function getOpenPopupIds() {
  return POPUP_IDS.filter((id) => {
    const popup = byId(id);
    return popup && !popup.hidden;
  });
}

function syncPopupPageState() {
  document.body.classList.toggle("modal-open", getOpenPopupIds().length > 0);
  const profileFabBtn = byId("profileFabBtn");
  const profileModal = byId("profileModal");
  if (profileFabBtn && profileModal) {
    profileFabBtn.setAttribute("aria-expanded", String(!profileModal.hidden));
  }
}

function positionProfileFab() {
  const profileFabBtn = byId("profileFabBtn");
  const brand = document.querySelector(".brand");
  if (!profileFabBtn || !brand) return;

  const brandRect = brand.getBoundingClientRect();
  const iconWidth = profileFabBtn.offsetWidth || 42;
  const minLeft = 8;
  const midpointLeft = (brandRect.left - iconWidth) / 2;
  const maxLeft = Math.max(minLeft, brandRect.left - iconWidth - 8);
  const targetLeft = Math.max(minLeft, Math.min(maxLeft, midpointLeft));

  profileFabBtn.style.left = `${Math.round(targetLeft)}px`;
}

function openPopup(popupId) {
  const popup = byId(popupId);
  if (!popup) return;

  POPUP_IDS.forEach((id) => {
    const node = byId(id);
    if (!node) return;
    node.hidden = id !== popupId;
  });

  syncPopupPageState();
}

function closePopup(popupId) {
  const popup = byId(popupId);
  if (!popup) return;
  popup.hidden = true;
  syncPopupPageState();
}

