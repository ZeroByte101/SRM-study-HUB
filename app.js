async function signIn(event) {
  event.preventDefault();
  const emailInput = byId("signInEmail");
  const passwordInput = byId("signInPassword");
  const status = byId("signInStatus");
  if (!emailInput || !passwordInput || !status) return;

  const email = emailInput.value.trim();
  const password = passwordInput.value;
  if (!email || !password) {
    status.textContent = "Enter email and password.";
    return;
  }

  status.textContent = "Signing in...";
  try {
    const response = await fetch(apiUrl("/auth/signin"), {
      method: "POST",
      headers: { "Content-Type": "application/json" },
      body: JSON.stringify({ email, password })
    });
    const payload = await response.json();

    if (!response.ok || !payload.token) {
      status.textContent = payload && payload.error ? payload.error : "Sign in failed.";
      return;
    }

    setAuthToken(payload.token);
    renderAuthState(payload.user || null);
    status.textContent = "Signed in.";
    emailInput.value = "";
    passwordInput.value = "";
    const signUpStatus = byId("signUpStatus");
    if (signUpStatus) signUpStatus.textContent = "";
    await loadProfile();
  } catch (_err) {
    status.textContent = "Network error while signing in.";
  }
}

