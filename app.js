async function searchTopic() {
  const topicInput = byId("topic");
  const results = byId("results");
  const videoError = byId("videoError");
  if (!topicInput || !results || !videoError) return;

  const topic = topicInput.value.trim();
  videoError.textContent = "";
  results.innerHTML = "";

  if (!topic) {
    videoError.textContent = "Enter a topic to search.";
    return;
  }

  try {
    const response = await fetch(apiUrl(`/videos/${encodeURIComponent(topic)}`));
    const data = await response.json();

    if (!response.ok) {
      videoError.textContent = data && data.error ? data.error : "Unable to fetch resources now.";
      return;
    }

    if (!Array.isArray(data) || !data.length) {
      videoError.textContent = "No resources found for this topic.";
      return;
    }

    data.forEach((video) => {
      const item = document.createElement("div");
      item.className = "video-item";

      const link = document.createElement("a");
      link.href = video.url;
      link.target = "_blank";
      link.rel = "noopener";
      link.textContent = video.title;

      item.appendChild(link);
      results.appendChild(item);
    });
  } catch (_err) {
    videoError.textContent = "Network error while searching resources.";
  }
}

